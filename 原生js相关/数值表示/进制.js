const num1 = 100; // 十进制
// binary
const num2 = 0b100; //二进制     二的二次方   4
// octonary
const num3 = 0o100; //八进制     64
// hexadecimal
const num4 = 0x100; //十六进制   256

console.log(num1, num2, num3, num4);

